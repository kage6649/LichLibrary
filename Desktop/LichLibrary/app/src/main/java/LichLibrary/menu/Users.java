    private void delUsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delUsActionPerformed
        // TODO add your handling code here:
        MongoCollection<Document> col = c.db.getCollection("user");
        try {
            col.deleteOne(Filters.eq("username", Us));

            errUs.setText("Delete Success!");
            errUs.setForeground(Color.GREEN);
            errUs.setIcon(new ImageIcon(getClass().getResource("/icon/check.png")));
            errUs.setVisible(true);
            //delay showoff
            ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
            executorService.schedule(() -> {
                errUs.setVisible(false);
                pUs.setVisible(false);
                clUs.setVisible(false);
                errUs.setForeground(Color.red);
                errUs.setIcon(new ImageIcon(getClass().getResource("/icon/warning.png")));
            }, 5, TimeUnit.SECONDS);
            tableUs();
        } catch (Exception e) {
            errUs.setText("Delete Failed!");
            errUs.setVisible(true);
            ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
            executorService.schedule(() -> {
                errUs.setVisible(false);
            }, 5, TimeUnit.SECONDS);
        }
    }//GEN-LAST:event_delUsActionPerformed

