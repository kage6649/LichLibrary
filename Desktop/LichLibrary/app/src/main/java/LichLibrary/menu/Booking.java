    public void table_val(){
        MongoCollection<Document> book = c.db.getCollection("buku");
        MongoCollection<Document> boking = c.db.getCollection("peminjaman");
        
        DefaultTableModel dt = (DefaultTableModel) BKtable.getModel();
        dt.setRowCount(0);
        
//        AggregateIterable<Document> show = boking.aggregate(Arrays.asList(
//            new Document("$match",new Document("$or",Arrays.asList(
//            new Document("status", "Booking"),new Document("status", "Rejected")
//            )))
//        ));
        FindIterable<Document> show = boking.find(Filters.eq("status", "Booking")).sort(new Document("_id",-1)).limit(50);
        show.forEach(val -> {
            Vector v = new Vector();
            v.add(val.getObjectId("_id"));
            v.add(val.getString("username"));
            
            FindIterable<Document> show2 = book.find(Filters.eq("_id", val.getObjectId("_id_buku")));
            show2.forEach(val2-> {
                v.add(val2.getString("judul"));
            });
            v.add(val.getString("status"));
            v.add(val.getInteger("masa"));
            
            dt.addRow(v);
        });
    }
    private static String convertMongoDate(String val){
    SimpleDateFormat inputFormat = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy");
    SimpleDateFormat outputFormat= new SimpleDateFormat("E, MMM dd, yyyy");
    try {
        String finalStr = outputFormat.format(inputFormat.parse(val));
//        System.out.println(finalStr);
        return finalStr;
    } catch (Exception e) {
        e.printStackTrace();
    }
    return "";
}
    public static java.sql.Date addDaysToSQLDate(java.sql.Date date, int days) {
        
        return new java.sql.Date(date.getTime() + (days * DAY_IN_MILLSEC));
    }

