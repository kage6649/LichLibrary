    private void acctActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acctActionPerformed
        // TODO add your handling code here:\
        MongoCollection<Document> boking = c.db.getCollection("peminjaman");
        MongoCollection<Document> boking_d = c.db.getCollection("kategoribuku_relasi");
        //        MongoCollection<Document> pijam = c.db.getCollection("pinjam");
        //        Document in = new Document();
        
        FindIterable<Document> cek = boking.find(Filters.eq("_id", new ObjectId(bk)));
        cek.forEach(val -> {
            FindIterable<Document> cekstok = boking_d.find(Filters.eq("_id_buku", val.getObjectId("_id_buku")));
            cekstok.forEach(val2 ->{
                int x = val2.getInteger("stok");
                if (x>0) {
                    Calendar cal = Calendar.getInstance();
                    java.sql.Date date_now = new java.sql.Date(cal.getTime().getTime());
                    boking.updateMany(Filters.eq("_id", val.getObjectId("_id")), Arrays.asList(
                        Updates.set("status", "Dipinjam"),
                        Updates.set("tgl_pinjam", date_now),
                        Updates.set("tgl_kembali", addDaysToSQLDate(date_now, val.getInteger("masa")))
                    ));
                    boking_d.updateOne(Filters.eq("_id_buku", val.getObjectId("_id_buku")), Updates.inc("stok", -1));
                    err.setText("Sucess Borwow Book!");
                    err.setForeground(Color.GREEN);
                    err.setIcon(new ImageIcon(getClass().getResource("/icon/check.png")));
                    err.setVisible(true);
                    //delay showoff
                    ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                    executorService.schedule(() -> {
                        err.setVisible(false);
                        err.setForeground(Color.red);
                        err.setIcon(new ImageIcon(getClass().getResource("/icon/warning.png")));
                    }, 5, TimeUnit.SECONDS);
                }else{
                    err.setText("Out Of Stock Book!");
                    err.setVisible(true);
                    //delay showoff
                    ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                    executorService.schedule(() -> {
                    err.setVisible(false);
                    act.setVisible(false);
                    reject.setVisible(false);
                    }, 5, TimeUnit.SECONDS);
                }
            });
        });
        table_val();
    }//GEN-LAST:event_acctActionPerformed

    private void rejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectActionPerformed
        // TODO add your handling code here:
        MongoCollection<Document> boking = c.db.getCollection("peminjaman");
        //        boking.deleteOne(Filters.eq("_id",new ObjectId(bk)));
        boking.updateOne(Filters.eq("_id", new ObjectId(bk)), Updates.set("status", "Rejected"));
        err.setText("Sucess Reject Data!");
        err.setForeground(Color.GREEN);
        err.setIcon(new ImageIcon(getClass().getResource("/icon/check.png")));
        err.setVisible(true);
        //delay showoff
        ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
        executorService.schedule(() -> {
            err.setVisible(false);
            err.setForeground(Color.red);
            err.setIcon(new ImageIcon(getClass().getResource("/icon/warning.png")));
        }, 5, TimeUnit.SECONDS);
        table_val();
    }//GEN-LAST:event_rejectActionPerformed

