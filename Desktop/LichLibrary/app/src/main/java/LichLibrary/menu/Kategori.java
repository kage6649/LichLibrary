    public void table_val(){
        MongoCollection<Document> col = c.db.getCollection("kategoribuku");
        MongoCollection<Document> col2 = c.db.getCollection("buku");
        DefaultTableModel dt = (DefaultTableModel) Gtable.getModel();
        dt.setRowCount(0);
        
         FindIterable<Document> show = col.find().sort(new Document("_id",-1));
         show.forEach(val ->{
              Vector v = new Vector();
              v.add(val.getString("NamaKategori"));
              long count = col2.countDocuments(Filters.eq("kategori", val.getString("NamaKategori")));
              if(count!=0){
                  v.add(count);
              }else{
                  v.add("Kosong");
              }
                dt.addRow(v);
         });
        
    }

