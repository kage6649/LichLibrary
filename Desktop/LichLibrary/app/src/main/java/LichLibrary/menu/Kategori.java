    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO add your handling code here:
        MongoCollection<Document> col = c.db.getCollection("kategoribuku");
        MongoCollection<Document> col2 = c.db.getCollection("buku");
        Document del = new Document();
        del.put("NamaKategori", g);
        long cek = col2.countDocuments(Filters.eq("kategori", g));
        if(cek!=0){
            try {
                c.con = DriverManager.getConnection(c.url);
                c.stm = c.con.createStatement();
                String q = "insert into kategori(kategori) values ('"+g+"')";
                c.stm.executeUpdate(q);
                r.toKpop();
                c.stm.close();c.con.close();
            } catch (Exception e) {
                err.setText("Error Delete Data!");
                err.setVisible(true);
                //delay showoff
                ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                executorService.schedule(() -> {
                    err.setVisible(false);
                }, 5, TimeUnit.SECONDS);
            }
        }else{
            col.deleteOne(del);
        }
        table_val();
        act.setVisible(false);
    }//GEN-LAST:event_delActionPerformed

