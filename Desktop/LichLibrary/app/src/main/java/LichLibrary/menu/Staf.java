    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if(user.getText().equals("")||pass.getPassword().equals("")||name.getText().equals("")||mail.getText().equals("")||address.getText().equals("")){
            err.setText("Entry All Colomn!");
            err.setVisible(true);
            //delay showoff
            ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
            executorService.schedule(() -> {
                err.setVisible(false);
            }, 5, TimeUnit.SECONDS);
        }else{
            ED ed = new ED();
            MongoCollection<Document> col = c.db.getCollection("user");
            Document in = new Document();
            String pas = new String(pass.getPassword());
            String epas = ed.encryptionKey(pas);
            in.put("username", user.getText());
            in.put("password", epas);
            in.put("NamaLengkap", name.getText());
            in.put("email", mail.getText());
            in.put("Alamat", address.getText());
            in.put("level", "staf");

            try {
                col.insertOne(in);
                err.setText("Success Insert Data!");
                err.setForeground(Color.GREEN);
                err.setIcon(new ImageIcon(getClass().getResource("/icon/check.png")));
                err.setVisible(true);
                TableValue();
                //delay showoff
                ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                executorService.schedule(() -> {
                    err.setVisible(false);
                    err.setForeground(Color.red);
                    err.setIcon(new ImageIcon(getClass().getResource("/icon/warning.png")));
                }, 5, TimeUnit.SECONDS);
                user.setText("");
                pass.setText("");
                name.setText("");
                mail.setText("");
                address.setText("");
                
            } catch (Exception e) {
                err.setText("Error Insert Data!");
                err.setVisible(true);
                //delay showoff
                ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                executorService.schedule(() -> {
                    err.setVisible(false);
                }, 5, TimeUnit.SECONDS);
            }}
            act.setVisible(false);
    }//GEN-LAST:event_saveActionPerformed

