    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        MongoCollection<Document> col = c.db.getCollection("user");
        Document del = new Document();
        del.put("username", USkey);
        del.put("level", "staf");
        try {
            col.deleteOne(del);
            err.setText("Success Delete Data!");
            err.setForeground(Color.GREEN);
            err.setIcon(new ImageIcon(getClass().getResource("/icon/check.png")));
            err.setVisible(true);
            TableValue();
            //delay showoff
            ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
            executorService.schedule(() -> {
                err.setVisible(false);
                err.setForeground(Color.red);
                err.setIcon(new ImageIcon(getClass().getResource("/icon/warning.png")));
            }, 5, TimeUnit.SECONDS);
        } catch (Exception e) {
            err.setText("Error Delete Data!");
            err.setVisible(true);
            //delay showoff
            ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
            executorService.schedule(() -> {
                err.setVisible(false);
            }, 5, TimeUnit.SECONDS);
        }
        act.setVisible(false);
    }//GEN-LAST:event_deleteActionPerformed

