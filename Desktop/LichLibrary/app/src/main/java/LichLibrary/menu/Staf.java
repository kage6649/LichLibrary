    public void TableValue(){
         MongoCollection<Document> col = c.db.getCollection("user");
         
         DefaultTableModel dt = (DefaultTableModel) table.getModel();
         dt.setRowCount(0);
         
        FindIterable<Document> show = col.find(Filters.eq("level","staf"));
        show.forEach(val -> {
            Vector v = new Vector();
                v.add(val.getString("username"));
                v.add(val.getString("NamaLengkap"));
                v.add(val.getString("email"));
                v.add(val.getString("Alamat"));
                
                dt.addRow(v);
        });
    }

