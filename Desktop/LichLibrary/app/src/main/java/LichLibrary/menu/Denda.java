    private void DendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DendaActionPerformed
        // TODO add your handling code here:
        MongoCollection<Document> col = c.db.getCollection("denda");
        if (idenda.getText().equals("")||dch.getSelectedItem().toString().equals("-- Pilih Jenis --")) {
            err.setText("Choose Denda Type or Fill the Field!");
            err.setVisible(true);
            ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
            executorService.schedule(() -> {
                err.setVisible(false);
            }, 5, TimeUnit.SECONDS);
        } else {
            int d = Integer.parseInt(idenda.getText());
            String type = dch.getSelectedItem().toString();

            if (type.equals("Telat")) {
                try {
                    col.updateOne(Filters.eq("num", 1), Updates.set("denda", d));
                    err.setText("Denda Changed!");
                    err.setForeground(Color.GREEN);
                    err.setIcon(new ImageIcon(getClass().getResource("/icon/check.png")));
                    err.setVisible(true);
                    //delay showoff
                    ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                    executorService.schedule(() -> {
                        err.setVisible(false);
                        err.setForeground(Color.red);
                        err.setIcon(new ImageIcon(getClass().getResource("/icon/warning.png")));
                    }, 5, TimeUnit.SECONDS);
                    idenda.setText("");
                    info_denda();
                } catch (Exception e) {
                    err.setText("Denda Change Failed!");
                    err.setVisible(true);
                    ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                    executorService.schedule(() -> {
                        err.setVisible(false);
                    }, 5, TimeUnit.SECONDS);
                }
            } else if (type.equals("Hilang")){
                try {
                    col.updateOne(Filters.eq("num", 2), Updates.set("denda", d));
                    err.setText("Denda Changed!");
                    err.setForeground(Color.GREEN);
                    err.setIcon(new ImageIcon(getClass().getResource("/icon/check.png")));
                    err.setVisible(true);
                    //delay showoff
                    ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                    executorService.schedule(() -> {
                        err.setVisible(false);
                        err.setForeground(Color.red);
                        err.setIcon(new ImageIcon(getClass().getResource("/icon/warning.png")));
                    }, 5, TimeUnit.SECONDS);
                    idenda.setText("");
                    info_denda();
                } catch (Exception e) {
                    err.setText("Denda Change Failed!");
                    err.setVisible(true);
                    ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                    executorService.schedule(() -> {
                        err.setVisible(false);
                    }, 5, TimeUnit.SECONDS);
                }
            } else {
                err.setText("Denda Change Failed!");
                err.setVisible(true);
                ScheduledExecutorService executorService = Executors.newSingleThreadScheduledExecutor();
                executorService.schedule(() -> {
                    err.setVisible(false);
                }, 5, TimeUnit.SECONDS);
            }

        }
        dch.setSelectedIndex(0);
    }//GEN-LAST:event_DendaActionPerformed

