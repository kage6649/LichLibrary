    public void info_denda(){
         MongoCollection<Document> col = c.db.getCollection("denda");
         FindIterable<Document> dn = col.find(Filters.eq("num",1));
         FindIterable<Document> dn2 = col.find(Filters.eq("num",2));
         dn.forEach(d -> {
             int denda = d.getInteger("denda");
             if (denda!=0) {
                ndenda.setText("Rp "+denda);
            }else{
                ndenda.setText("Rp 0");
            }
         });
         dn2.forEach(d2 -> {
             int denda2 = d2.getInteger("denda");
             if (denda2!=0) {
                ndendah.setText("Rp "+denda2);
            }else{
                ndendah.setText("Rp 0");
            }
         });
    }

